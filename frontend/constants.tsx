// Helper to ensure API URL has protocol
const getApiUrl = () => {
  const url = import.meta.env.VITE_API_URL || 'http://localhost:8080';
  if (url.startsWith('http')) return url;
  return `https://${url}`;
};

export const API_URL = getApiUrl();

